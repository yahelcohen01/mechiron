function isActive(pathname: string, href: string, matchExact: boolean): boolean {
  if (matchExact) return pathname === href;
  return pathname.startsWith(href);
}

export function SidebarNav({ onNavigate }: { onNavigate?: () => void }) {
  const pathname = usePathname();
  const t = useT();

  return (
    <nav className="flex flex-col gap-1 flex-1">
      {navItems.map((item) => {
        const active = isActive(pathname, item.href, item.matchExact);
        return (
          <Link
            key={item.href}
            href={item.href}
            onClick={onNavigate}
            className={`flex items-center gap-3 rounded-md px-3 py-2 text-sm font-medium transition-colors ${
              active
                ? 'bg-blue-50 dark:bg-blue-950/50 text-blue-700 dark:text-blue-300 font-semibold'
                : 'text-gray-700 dark:text-gray-300 hover:text-gray-900 dark:hover:text-gray-100 hover:bg-gray-100 dark:hover:bg-gray-800'
            }`}
          >
            {item.icon}
            <span>{t.sidebar[item.labelKey]}</span>
          </Link>
        );
      })}
    </nav>
  );
}
